//switch, case, break, default
var number = prompt("Enter your number: ");
switch(number){
    case "0" :
        document.write("zero")
        break;
    case "1" :
        document.write("one")
        break;
    case "2" :
        document.write("Two")
        break;
    case "3" :
        document.write("Three")
        break;
    default:
        document.write("not a digit");
}